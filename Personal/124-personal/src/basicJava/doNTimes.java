package basicJava;

public class doNTimes {
    public static void main(String[] args){
        for(int i=10;i>0;i--){
            System.out.println(i);
        }
        System.out.println("First looping complete.\n");
        for(int i=4; i>=1; i--){
            System.out.println(i*8);
        }
    }
}
